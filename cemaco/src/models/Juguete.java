package models;
public class Juguete {
	
	private int codigo;
	private double valorUnitario;
	private int edadUsuario;
	private int numeroProveedor;
	private String tipoJuguete;
	private double valorAdicional;
	private int complejidad;
	/**
	 * @return the codigo
	 */
	public int getCodigo() {
		return codigo;
	}
	/**
	 * @param codigo the codigo to set
	 */
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	/**
	 * @return the valorUnitario
	 */
	public double getValorUnitario() {
		return valorUnitario;
	}
	/**
	 * @param valorUnitario the valorUnitario to set
	 */
	public void setValorUnitario(double valorUnitario) {
		this.valorUnitario = valorUnitario;
	}
	/**
	 * @return the edadUsuario
	 */
	public int getEdadUsuario() {
		return edadUsuario;
	}
	/**
	 * @param edadUsuario the edadUsuario to set
	 */
	public void setEdadUsuario(int edadUsuario) {
		this.edadUsuario = edadUsuario;
	}
	/**
	 * @return the numeroProveedor
	 */
	public int getNumeroProveedor() {
		return numeroProveedor;
	}
	/**
	 * @param numeroProveedor the numeroProveedor to set
	 */
	public void setNumeroProveedor(int numeroProveedor) {
		this.numeroProveedor = numeroProveedor;
	}
	/**
	 * @return the tipoJuguete
	 */
	public String getTipoJuguete() {
		return tipoJuguete;
	}
	/**
	 * @param tipoJuguete the tipoJuguete to set
	 */
	public void setTipoJuguete(String tipoJuguete) {
		this.tipoJuguete = tipoJuguete;
	}
	/**
	 * @return the valorAdicional
	 */
	public double getValorAdicional() {
		return valorAdicional;
	}
	/**
	 * @param valorAdicional the valorAdicional to set
	 */
	public void setValorAdicional(double valorAdicional) {
		this.valorAdicional = valorAdicional;
	}
	/**
	 * @return the complejidad
	 */
	public int getComplejidad() {
		return complejidad;
	}
	/**
	 * @param complejidad the complejidad to set
	 */
	public void setComplejidad(int complejidad) {
		this.complejidad = complejidad;
	}
	
}
